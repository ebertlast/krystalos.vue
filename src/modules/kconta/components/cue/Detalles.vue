      CUENTA: undefined,
      NOMCUENTA: undefined,
      TIPO: undefined,
      NEMONICO: undefined,
      CENTRO: undefined,
      PRODUCTO: undefined,
      REFERENCIA: undefined,
      TERCERO: undefined,
      RETENCION: undefined,
      CPTORETENCION: undefined,
      AJUSTE: undefined,
      CIERRE: undefined,
      CUENTAFISCAL: undefined,
      IMPUESTO: undefined,
