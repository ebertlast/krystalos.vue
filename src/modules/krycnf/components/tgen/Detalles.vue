import { mapActions, mapGetters } from "vuex";
export default {
  props: ["fila"],
  data: () => ({
    cargando: false,
    model: {
      TABLA: undefined,
      CAMPO: undefined,
      CODIGO: undefined,
      DESCRIPCION: undefined,
      VALOR1: undefined,
      DATO1: undefined,
      DFECHA: undefined,
      OPCIONESLIBRES: undefined,
      VALORINI: undefined,
      VALORFIN: undefined,
      VALOR2: undefined,
      DATO2: undefined
    }
  }),
  mounted() {
    this.refrescarDatos();
  },
  methods: {
    ...mapActions(["notificacion"]),
    refrescarDatos() {
      if (
        !this.fila ||
        !this.fila.CODIGO ||
        !this.fila.CAMPO ||
        !this.fila.TABLA
      )
        return;
      this.cargando = true;
      this.model = {};
      this.$http
        .get(`tgen/${this.fila.TABLA}/${this.fila.CAMPO}/${this.fila.CODIGO}`)
        .then(res => {
          this.model = res.result.recordset[0];
        })
        .then(() => {
          this.cargando = false;
        })
        .catch(err => {
          this.cargando = false;
        });
    },
    seleccionar() {
      this.$emit("seleccionar", this.model);
    },
    eliminar() {
      // return this.notificacion({
      //   message:
      //     "No est√° permitido eliminar un registro de esta tabla, intente inactivarlo",
      //   type: "error"
      // });
      this.$emit("eliminar", this.model);
    },
    cancelar() {
      this.$emit("cancelar");
    },
    editarFila() {
      this.$emit("editar", this.model);
    }
  },
  watch: {
    fila() {
      this.refrescarDatos();
    }
  }
};
